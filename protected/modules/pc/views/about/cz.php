   <script>
   $(function(){
	   $("#submit_button").click(function(){
			var username=$("#paymentform").find("input[name='username']").val();
			var re_username=$("#paymentform").find("input[name='re_username']").val();
			var radio_on=$(".pay_money_conn").find("input[type='radio']:checked").val();
			var fee1=$("#paymentform").find("input[name='fee1']").val();
			var fee1=parseInt(fee1);
			
			if(!username){
				alert("请输入充值账号");
				return false;
			} 
			
			if(!re_username){
				alert("请输入重复账号");
				return false;
			}

			if(radio_on==-1){
				if(isNaN(fee1)||!fee1){
					alert("请填写1到10000之间的整数");
					return false;
				}
			}

			var url='/';
			if($(".everybank").is(":hidden")){
				var li_obj=$(".L_menu").find("li");
				var li_len=$(li_obj).length;
				var ind=0;
				for(var i=0;i<li_len;i++){
					if($(li_obj[i]).hasClass("on")){
						ind=i;
					}
				}
				if(ind==2){
					url="https://pay.weixin.qq.com/";
				}else if(ind==3){
					url="https://www.alipay.com/";
				}else if(ind==4){
					url="https://www.tenpay.com/";
				}								
			}else{
				url=$(".pay_bank_conn").find("input[type='radio']:checked").attr('rel');
			}
			window.location.href=url;		
		 })
	  })
   </script>